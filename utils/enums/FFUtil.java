package utils.enums;

/**
 * Available FFmpeg tools
 * 
 * @author cristopher
 */
public enum FFUtil {
    /**
     * FFmpeg is a tool used to record, convert, and stream audio and video 
     * files (and many other things)
     */
    FFMPEG, 

    /**
     * FFplay is a media player. It can play back audio and video files, as 
     * well as streams
     */
    FFPLAY, 

    /**
     * FFprobe is a tool that gathers information about media files, such 
     * as format, codec, resolution, duration and many others properties
     */
    FFPROBE
}
